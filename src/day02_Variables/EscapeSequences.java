package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\t\tMuhtar");

            // it means a paragraph space
        System.out.println("Cybertek School");
        System.out.println("Cybertek \nSchool");
        System.out.println("\n\n\nMy\nName\nIs\nAli");

            // it means new line, starts with new line

        // print : My favorite show is "Game of Thrones"

        System.out.println("My Favorite TV show is \"Game of Thrones\" ");

        System.out.println("My favorite book is \"Catch in the Rye\" ");

        // it means double quote, prints double quote on the console

        System.out.println("\\");

                // it prints single slash on console
        System.out.println("/");

        System.out.println("My favorite book is 'Java' ");  // \' prints single quote
        System.out.println("My favorite book is \'Java\' "); // prints '


    }
}
